const month = "April"
switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("February");
        break;
    case "march":
        console.log("March");
        break;
    case "April":
        console.log("April123");
        break;
    case 5:
        console.log("May");
        break;

    default:
        break;
}
